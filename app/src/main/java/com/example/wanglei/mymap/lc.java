public class lc {
    private String name;
    private String jd;
    private String wd;
    private String jg;
    private String ms;


    public lc() {
    }

    public lc(String name, String jd, String wd,String jg, String ms) {
        this.name = name;
        this.jd = jd;
        this.wd = wd;
        this.jg = jg;
        this.ms = ms;

    }

    public String getname() {
        return name;
    }

    public String getjd() {
        return jd;
    }
    public String getwd()  {
        return wd;
    }
    public String getjg() {
        return jg;
    }
    public String getms()  {
        return ms;
    }






}
